@router.get("/o/{tracking_id}")
async def track_open(
    tracking_id: str,
    request: Request,
    db: AsyncSession = Depends(get_db),
) -> Response:
    """Record an email-open event and return a 1x1 transparent pixel.

    The tracking_id encodes the campaign and recipient. The event is
    persisted to the database and pushed to the Redis real-time feed.
    """
    _pixel_response = Response(
        content=_TRACKING_PIXEL,
        media_type="image/gif",
        headers={
            "Cache-Control": "no-store, no-cache, must-revalidate, max-age=0",
        },
    )

    if not _validate_tracking_id(tracking_id):
        return _pixel_response

    campaign_id, token = _parse_tracking_id(tracking_id)
    if campaign_id is None:
        return _pixel_response

    ip = request.client.host if request.client else "unknown"
    ua = request.headers.get("user-agent", "")
    metadata = {"user_agent": ua, "ip_address": ip}

    # Graceful degradation: if DB/Redis write fails, still return the pixel.
    redis_client = None
    try:
        recorder, redis_client = _get_recorder()
        await recorder.record_open(campaign_id, token, metadata, db)
    except Exception:
        logger.warning("Failed to record open event for %s (non-fatal)", tracking_id, exc_info=True)
    finally:
        if redis_client is not None:
            try:
                await redis_client.aclose()
            except Exception:
                pass

    return _pixel_response


@router.get("/c/{tracking_id}")
async def track_click(
    tracking_id: str,
    request: Request,
    db: AsyncSession = Depends(get_db),
) -> Response:
    """Record a link-click event and redirect to the landing page.

    After logging the click event, redirects the user to the campaign's
    landing page. If no landing page is configured but a
    training_redirect_url is set, redirects there directly.

    Returns a consistent response regardless of token validity to
    prevent enumeration.
    """
    if not _validate_tracking_id(tracking_id):
        return HTMLResponse(content=_THANK_YOU_HTML, status_code=200)

    campaign_id, token = _parse_tracking_id(tracking_id)

    ip = request.client.host if request.client else "unknown"
    ua = request.headers.get("user-agent", "")
    url = str(request.url)
    metadata = {"user_agent": ua, "ip_address": ip}

    # Persist click event (graceful degradation).
    if campaign_id is not None:
        redis_client = None
        try:
            recorder, redis_client = _get_recorder()
            await recorder.record_click(campaign_id, token, url, metadata, db)
        except Exception:
            logger.warning("Failed to record click event for %s (non-fatal)", tracking_id, exc_info=True)
        finally:
            if redis_client is not None:
                try:
                    await redis_client.aclose()
                except Exception:
                    pass

    if campaign_id is not None:
        campaign = await db.get(Campaign, campaign_id)
        if campaign is not None:
            # If a landing page is configured, the click redirects there.
            # The landing page form submission handles the training redirect.
            if campaign.landing_page_id is not None:
                # TODO: resolve landing page URL from campaign.landing_page
                pass
            elif campaign.training_redirect_url:
                # No landing page -- redirect directly to training.
                return RedirectResponse(
                    url=campaign.training_redirect_url,
                    status_code=302,
                )

    return HTMLResponse(content=_THANK_YOU_HTML, status_code=200)


@router.post("/s/{tracking_id}")
async def track_submission(
    tracking_id: str,
    payload: SubmissionPayload,
    request: Request,
    db: AsyncSession = Depends(get_db),
) -> Response:
    """Record credentials or form data submitted from a landing page.

    After logging the submission, redirects the user to the campaign's
    training_redirect_url if one is configured. Otherwise returns a
    minimal thank-you page.

    Privacy safeguards:
    - Only field NAMES (keys) are logged, never values.
    - The raw request body is scrubbed from the ASGI scope after processing.
    - The response is identical regardless of token validity.
    """
    if not _validate_tracking_id(tracking_id):
        # Scrub body and return consistent response.
        request.scope.pop("body", None)
        return HTMLResponse(content=_THANK_YOU_HTML, status_code=200)

    # Log only field names for audit trail -- never the values.
    field_names = list(payload.fields.keys())
    logger.info(
        "Credential submission received: tracking_id=%s field_names=%s",
        tracking_id,
        field_names,
    )

    campaign_id, token = _parse_tracking_id(tracking_id)

    ip = request.client.host if request.client else "unknown"
    ua = request.headers.get("user-agent", "")
    metadata = {"user_agent": ua, "ip_address": ip}

    # Persist submission event (graceful degradation).
    if campaign_id is not None:
        redis_client = None
        try:
            recorder, redis_client = _get_recorder()
            await recorder.record_submission(campaign_id, token, field_names, metadata, db)
        except Exception:
            logger.warning("Failed to record submission event for %s (non-fatal)", tracking_id, exc_info=True)
        finally:
            if redis_client is not None:
                try:
                    await redis_client.aclose()
                except Exception:
                    pass

    # Scrub the raw submission body from the ASGI scope so that upstream
    # middleware (access logs, error reporters) cannot inadvertently leak
    # credential data.
    request.scope.pop("body", None)
    request._body = b""  # noqa: SLF001  -- intentional scope cleanup

    # Redirect to the campaign's training URL if configured.
    if campaign_id is not None:
        campaign = await db.get(Campaign, campaign_id)
        if campaign is not None and campaign.training_redirect_url:
            return RedirectResponse(
                url=campaign.training_redirect_url,
                status_code=302,
            )

    return HTMLResponse(content=_THANK_YOU_HTML, status_code=200)
